// ' ' (x20), '\t' (0x09), '\n' (0x0a), '\v' (0x0b), '\f' (0x0c), '\r' (0x0d)
int	is_whitespace(char c)
{
	if (c == ' ')
		return (1);
	if (c >= '\t' && c <= '\r')
		return (1);
	return (0);
}
